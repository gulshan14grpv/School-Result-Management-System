    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String RollNumber=jTextField1.getText();
         try{
             Connection con=ConnectionProvider.getCon();
             Statement st=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
             ResultSet rs=st.executeQuery("SELECT * FROM student INNER JOIN result WHERE student.RollNumber='"+RollNumber+"' AND result.RollNumber='"+RollNumber+"'");
             if(!rs.first()){
                 JOptionPane.showMessageDialog(null,"Roll Number doesn't exist");
             }
             else{
                 name.setText(rs.getString(1));
                 gender.setText(rs.getString(3));
                 course.setText(rs.getString(4));
                 fn.setText(rs.getString(5));
                 branch.setText(rs.getString(6));
                 
                 phy.setText(rs.getString(8));
                 math.setText(rs.getString(9));
                 chem.setText(rs.getString(10));
                 elec.setText(rs.getString(11));
                 graph.setText(rs.getString(12));
                 
                 total.setText(rs.getString(13));
                 
                 int m1 = Integer.parseInt(rs.getString(8));
                 int m2 = Integer.parseInt(rs.getString(9));
                 int m3 = Integer.parseInt(rs.getString(10));
                 int m4 = Integer.parseInt(rs.getString(11));
                 int m5 = Integer.parseInt(rs.getString(12));
                 
                if(m1<30 || m2<30 || m3<30 || m4<30 || m5<30) {
                verdict.setText("Fail");
                }
                else{
                verdict.setText("Pass");
                }
                
                 name.setEditable(false);
                  gender.setEditable(false);
                  course.setEditable(false);
                  fn.setEditable(false);
                  branch.setEditable(false);
                  phy.setEditable(false); 
                  math.setEditable(false);
                   chem.setEditable(false);
                    elec.setEditable(false);
                     graph.setEditable(false);
                      total.setEditable(false);
                       verdict.setEditable(false);
             }   
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

