    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String RollNumber=jTextField1.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs=st.executeQuery("SELECT * FROM student WHERE RollNumber='"+RollNumber+"'");

            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null,"Entered RollNumber doesn't  exist");
            }
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
            
    }//GEN-LAST:event_searchActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String RollNumber=jTextField1.getText();
        int physicsMarks = Integer.parseInt(jTextField2.getText());
        int mathMarks = Integer.parseInt(jTextField3.getText());
        int chemMarks = Integer.parseInt(jTextField4.getText()); 
        int electricalMarks = Integer.parseInt(jTextField5.getText());
        int graphMarks = Integer.parseInt(jTextField6.getText());
        int result=0;
           result=physicsMarks+mathMarks+chemMarks+electricalMarks+graphMarks;
          
         try{
             Connection con=ConnectionProvider.getCon();
             Statement st=con.createStatement();
           
             st.executeUpdate("INSERT INTO result VALUES ('"+RollNumber+"','"+physicsMarks+"','"+mathMarks+"','"+chemMarks+"','"+electricalMarks+"','"+graphMarks+"','"+result+"')");
         JOptionPane.showMessageDialog(null, "Sucessfully Added");
         setVisible(false);
         AddResult frame=new AddResult();
         frame.setVisible(true);
         frame.setLocationRelativeTo(null);
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(null,e.toString());
         }
    }//GEN-LAST:event_jButton1ActionPerformed

