    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        
        String RollNumber=jTextField1.getText();
        try{
            Connection con=ConnectionProvider.getCon();
           
            Statement st=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs=st.executeQuery("SELECT * FROM result WHERE RollNumber='"+RollNumber+"'");
            if(!rs.first()){
            JOptionPane.showMessageDialog(null,"Roll Number doesn't exist");
            }
            else{
            jTextField2.setText(rs.getString(2));
            jTextField3.setText(rs.getString(3));
            jTextField4.setText(rs.getString(4));
            jTextField5.setText(rs.getString(5));
            jTextField6.setText(rs.getString(6));
            jTextField1.setEditable(false);           
            }
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_searchActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
           String RollNumber=jTextField1.getText();
         int physicsMarks = Integer.parseInt(jTextField2.getText());
        int mathMarks = Integer.parseInt(jTextField3.getText());
        int chemMarks = Integer.parseInt(jTextField4.getText()); 
        int electricalMarks = Integer.parseInt(jTextField5.getText());
        int graphMarks = Integer.parseInt(jTextField6.getText());
        int result=0;
           result=physicsMarks+mathMarks+chemMarks+electricalMarks+graphMarks;
          
         try{
             Connection con=ConnectionProvider.getCon();
             Statement st=con.createStatement();
             st.executeUpdate("UPDATE result SET physics="+physicsMarks+",maths="+mathMarks+",chem="+chemMarks+",electrical="+electricalMarks+",graph="+graphMarks+",result="+result+" WHERE RollNumber='"+RollNumber+"'");
              JOptionPane.showMessageDialog(null, "Sucessfully Updated");
             setVisible(false);
            UpdateStudentResult frame=new UpdateStudentResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
         }
         
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

